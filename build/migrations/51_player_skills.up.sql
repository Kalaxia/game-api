CREATE TABLE IF NOT EXISTS player__skills(
    id SERIAL PRIMARY KEY,
    combat SMALLINT NOT NULL DEFAULT 0,
    industry SMALLINT NOT NULL DEFAULT 0,
    politics SMALLINT NOT NULL DEFAULT 0,
    production SMALLINT NOT NULL DEFAULT 0,
    trade SMALLINT NOT NULL DEFAULT 0
);

ALTER TABLE players ADD skills_id INT REFERENCES player__skills(id) ON DELETE CASCADE;